def clear_paragraph(paragraph):
    paragraph.text = ""


def populate_report(template_path: Path, report: dict) -> Document:
    template = Document(str(template_path))
    output = Document()

    # Mirror document-wide settings from the template.
    output.styles.element[:] = deepcopy(template.styles.element[:])
    output._part._settings_part._element[:] = deepcopy(
        template._part._settings_part._element[:]
    )
    output.sections[0]._sectPr = deepcopy(template.sections[0]._sectPr)

    body = output._element.body
    for child in list(body):
        body.remove(child)

    def add_paragraph(text: str, style: str):
        paragraph = output.add_paragraph(style=style)
        paragraph.add_run(text)

    add_paragraph(
        f"Доповідь про виконану роботу за {report['date_label']}",
        "Title",
    )
    add_paragraph(f"Проєкт: {PROJECT_NAME}", "Normal")
    add_paragraph("Мета роботи за день", "Heading 1")
    add_paragraph(report["goal"], "Normal")
    add_paragraph("Виконані роботи", "Heading 1")

    for index, (heading, bullets) in enumerate(report["work_sections"], start=1):
        add_paragraph(f"{index}. {heading.split('. ', 1)[-1]}", "Heading 2")
        for bullet in bullets:
            add_paragraph(bullet, "Normal")

    add_paragraph("Результат на кінець дня", "Heading 1")
    for bullet in report["result"]:
        add_paragraph(bullet, "Normal")

    add_paragraph("Що ще не реалізовано", "Heading 1")
    for bullet in COMMON_NOT_IMPLEMENTED:
        add_paragraph(bullet, "Normal")

    add_paragraph("Висновок", "Heading 1")
    add_paragraph(report["conclusion"], "Normal")

    return output


def main():
    OUTPUT_DIR.mkdir(parents=True, exist_ok=True)

    for report in REPORTS:
        doc = populate_report(TEMPLATE_PATH, report)
        output_path = OUTPUT_DIR / report["file_name"]
        doc.save(output_path)
        print(output_path)


if __name__ == "__main__":
    main()
